#include "libft.h"

void	ft_bzero(void *b1, size_t c)
{
	char	*ch1;
	char	ch;

	ch1 = b1;
	ch = '\0';
	while (c > 0)
	{
		*ch1 = ch;
		ch1++;
		c--;
	}
}
